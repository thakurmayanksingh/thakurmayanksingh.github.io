// Horizontal projects scroller controls
function initProjectsScroller() {
    const track = document.querySelector('.projects-track');
    const prev = document.querySelector('.scroll-btn.prev');
    const next = document.querySelector('.scroll-btn.next');
    if (!track || !prev || !next) return;

    const scrollBy = () => Math.min(400, track.clientWidth * 0.8);

    prev.addEventListener('click', () => {
        track.scrollBy({ left: -scrollBy(), behavior: 'smooth' });
    });
    next.addEventListener('click', () => {
        track.scrollBy({ left: scrollBy(), behavior: 'smooth' });
    });
}

